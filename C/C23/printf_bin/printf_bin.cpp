#include "stdafx.h"

struct tagReadBin
{
	short int nLen;
	unsigned char c;
};

int main(int argc, char* argv[])
{
	unsigned char cData[48] = {
		0x05, 0x00, 0x48, 0x65, 0x6C, 0x6C, 0x6F, 0x06, 0x00, 0x42, 0x61, 0x63, 0x6B, 0x65, 0x72, 0x0D, 
			0x00, 0x77, 0x77, 0x77, 0x2E, 0x70, 0x65, 0x64, 0x69, 0x79, 0x2E, 0x63, 0x6F, 0x6D, 0x04, 0x00, 
			0x74, 0x65, 0x73, 0x74, 0x0A, 0x00, 0x4D, 0x65, 0x73, 0x73, 0x61, 0x67, 0x65, 0x42, 0x6F, 0x78
	};

	size_t nDateLen = sizeof(cData);
	int i = 0;
	struct tagReadBin readBin = {
		0,
		'\0'
	};
	while (i < nDateLen)
	{
		readBin.nLen = cData[i];
		int j;
		i += sizeof(short int);
		printf("%d ", readBin.nLen);
		for(j = 0; j < readBin.nLen; j++)
		{
			readBin.c = cData[i + j];
			printf("%c", readBin.c);
		}
		i += j;
		printf("\r\n");
	}
	getchar();
	return 0;
}

